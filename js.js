const bolu = (number, factor) => {
  if (number % factor === 0 && typeof(number === 'number')) {
    return `${factor} is a factor of ${number}`
  }
  else {
    return `${factor} is not a factor of ${number}`
  }
}
console.log(bolu(25, 5));

